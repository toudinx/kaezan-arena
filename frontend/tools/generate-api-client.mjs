#!/usr/bin/env node

import { exec } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const execAsync = promisify(exec);
const thisFilePath = fileURLToPath(import.meta.url);
const toolsDir = path.dirname(thisFilePath);
const projectRoot = path.resolve(toolsDir, "..");
const outputDir = path.join(projectRoot, "src", "app", "api", "generated");
const schemaOutputPath = path.join(outputDir, "schema.ts");
const clientOutputPath = path.join(outputDir, "client.ts");
const indexOutputPath = path.join(outputDir, "index.ts");
const defaultSwaggerUrl = "http://localhost:5158/swagger/v1/swagger.json";

const envSwaggerUrl = process.env.OPENAPI_URL?.trim();
const candidateSwaggerUrls = envSwaggerUrl
  ? [envSwaggerUrl]
  : [defaultSwaggerUrl];

async function resolveSwaggerUrl() {
  for (const url of candidateSwaggerUrls) {
    try {
      const response = await fetch(url, {
        method: "GET",
        signal: AbortSignal.timeout(4500)
      });
      if (!response.ok) {
        continue;
      }

      const bodySnippet = (await response.text()).slice(0, 512);
      if (bodySnippet.includes("\"openapi\"") || bodySnippet.includes("\"swagger\"")) {
        return url;
      }
    } catch {
      // Try next URL candidate.
    }
  }

  throw new Error(
    [
      "Could not reach a Swagger/OpenAPI JSON endpoint.",
      `Ensure backend is running on ${defaultSwaggerUrl} or set OPENAPI_URL.`,
      `Tried: ${candidateSwaggerUrls.join(", ")}`
    ].join(" ")
  );
}

async function generateSchema(swaggerUrl) {
  const command = `npx openapi-typescript "${swaggerUrl}" --output "${schemaOutputPath}"`;
  await execAsync(command, { cwd: projectRoot });
}

function generatedBanner() {
  return [
    "/* eslint-disable */",
    "// @generated by tools/generate-api-client.mjs",
    "// Do not edit this file manually.",
    ""
  ].join("\n");
}

async function writeGeneratedClientFiles() {
  const clientSource =
    generatedBanner() +
    [
      "import createClient from \"openapi-fetch\";",
      "import type { paths } from \"./schema\";",
      "",
      "export const apiClient = createClient<paths>({",
      "  baseUrl: \"\"",
      "});",
      ""
    ].join("\n");

  const indexSource =
    generatedBanner() +
    [
      "export { apiClient } from \"./client\";",
      "export type { components, operations, paths } from \"./schema\";",
      ""
    ].join("\n");

  await writeFile(clientOutputPath, clientSource, "utf8");
  await writeFile(indexOutputPath, indexSource, "utf8");
}

async function main() {
  await mkdir(outputDir, { recursive: true });
  const swaggerUrl = await resolveSwaggerUrl();
  await generateSchema(swaggerUrl);
  await writeGeneratedClientFiles();
  console.log(`[api:generate] OpenAPI source: ${swaggerUrl}`);
  console.log(`[api:generate] Generated client at: ${path.relative(projectRoot, outputDir)}`);
}

main().catch((error) => {
  console.error(`[api:generate] ${error instanceof Error ? error.message : String(error)}`);
  process.exitCode = 1;
});
